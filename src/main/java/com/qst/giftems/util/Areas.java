    public static String get(int provinceId, int cityId, int areaId) {
        String s = "";
        if (provinceId != 0) {
            for (Province p : provinces) {
                if (p.id == provinceId) {
                    s += p.name;
                    if (cityId != 0) {
                        for (City c : p.cities) {
                            if (c.id == cityId) {
                                s += " " + c.name;
                                if (areaId != 0) {
                                    for (Area a : c.areas) {
                                        if (a.id == areaId) {
                                            s += " " + a.name;
                                        }
                                    }
                                }
                            }
                        }
                    }
                }
            }
        }

        return s;
    }

